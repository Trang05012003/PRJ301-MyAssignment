package model;

/**
 *
 * @author X1 Nano
 */
public class Semester {
    private int id;
    private int year;

    // Constructor
    public Semester(int id, int year) {
        this.id = id;
        this.year = year;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
